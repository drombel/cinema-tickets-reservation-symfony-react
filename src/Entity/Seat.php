<?php

namespace App\Entity;

use App\Repository\SeatRepository;
use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\Common\Collections\Collection;
use Doctrine\ORM\Mapping as ORM;
use Symfony\Component\Validator\Constraints as Assert;
use JMS\Serializer\Annotation as JMS;

/**
 * @ORM\Entity(repositoryClass=SeatRepository::class)
 */
class Seat
{
    /**
     * @ORM\Id
     * @ORM\GeneratedValue
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="smallint")
     * @Assert\Positive()
     */
    private int $row;

    /**
     * @ORM\Column(type="smallint")
     * @Assert\Positive()
     */
    private int $col;

    /**
     * @ORM\ManyToOne(targetEntity="Room", inversedBy="seats")
     * @JMS\MaxDepth(1)
     */
    private ?Room $room;

    const STATUS_ACTIVE = 'ACTIVE';
    const STATUS_DISABLED = 'DISABLED';
    const STATUS_HIDDEN = 'HIDDEN';

    /**
     * @ORM\Column(type="string")
     */
    private string $status;

    /**
     * @ORM\OneToMany(targetEntity="Ticket", mappedBy="seat", cascade={"persist"})
     * @JMS\MaxDepth(1)
     */
    private Collection $tickets;

    public function __construct()
    {
        $this->tickets = new ArrayCollection();
    }

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getRow(): ?int
    {
        return $this->row;
    }

    public function setRow(int $row): self
    {
        $this->row = $row;

        return $this;
    }

    public function getCol(): ?int
    {
        return $this->col;
    }

    public function setCol(int $col): self
    {
        $this->col = $col;

        return $this;
    }

    public function getStatus(): ?string
    {
        return $this->status;
    }

    public function setStatus(string $status): self
    {
        if (!in_array($status, array(self::STATUS_ACTIVE, self::STATUS_DISABLED, self::STATUS_HIDDEN))) {
            throw new \InvalidArgumentException("Invalid status.");
        }
        $this->status = $status;

        return $this;
    }

    public function getRoom(): ?Room
    {
        return $this->room;
    }

    public function setRoom(?Room $room): self
    {
        $this->room = $room;

        return $this;
    }

    /**
     * @return Collection|Ticket[]
     */
    public function getTickets(): Collection
    {
        return $this->tickets;
    }

    public function addTicket(Ticket $ticket): self
    {
        if (!$this->tickets->contains($ticket)) {
            $this->tickets[] = $ticket;
            $ticket->setSeat($this);
        }

        return $this;
    }

    public function removeTicket(Ticket $ticket): self
    {
        if ($this->tickets->contains($ticket)) {
            $this->tickets->removeElement($ticket);
            // set the owning side to null (unless already changed)
            if ($ticket->getSeat() === $this) {
                $ticket->setSeat(null);
            }
        }

        return $this;
    }

    public function __toString()
    {
        return $this->getRoom().' ('.$this->col.','.$this->row.')';
    }
}
